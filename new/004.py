n = input('Digite qualquer coisa: ')
print('O que você digitou é:!',type(n))
print('O que você digitou está tudo em letras maiúsculas?:',n.isupper())
print('O que você digitou está tudo em letras minúsculas?',n.islower())
print('O que você digitou é um número?:',n.isnumeric())
print('O que você digitou é uma palavra?:',n.isalpha())
print('O que você digitou é alfanúmerico?:',n.isalnum())
print('O que você digitou está capitalizado?:',n.istitle())

